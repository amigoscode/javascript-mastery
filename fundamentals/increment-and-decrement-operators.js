// Increment and Decrement Operators
var number = 0;
// Postfix
console.log("postfix")
console.log(number++)
console.log(number)
console.log(number--)
console.log(number)
// Prefix
console.log("prefix")
var numberTwo = 0;
console.log(++numberTwo);
console.log(--numberTwo);

console.log("for loop")
for (var i = 0; i <= 10; i++) {
    console.log(i);
}

